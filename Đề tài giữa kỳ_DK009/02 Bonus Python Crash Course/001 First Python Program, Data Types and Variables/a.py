# Kiểu dữ liệu int (số nguyên)
num_int = 10
print(type(num_int))

# Kiểu dữ liệu float (số thực)
num_float = 3.14
print(type(num_float))

# Kiểu dữ liệu str (chuỗi)
str_example = "Hello, Python!"
print(type(str_example))

# Kiểu dữ liệu bool (boolean)
is_true = True
print(type(is_true))

# Kiểu dữ liệu list (danh sách)
list_example = [1, 2, 3, 4]
print(type(list_example))

# Kiểu dữ liệu tuple (bộ)
tuple_example = (1, "two", 3.0)
print(type(tuple_example))

# Kiểu dữ liệu set (tập hợp)
set_example = {1, 2, 3}
print(type(set_example))

# Kiểu dữ liệu dict (từ điển)
dict_example = {'key': 'value', 'name': 'John'}
print(type(dict_example))
