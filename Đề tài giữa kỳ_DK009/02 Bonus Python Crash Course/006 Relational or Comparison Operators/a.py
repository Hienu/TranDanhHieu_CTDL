# Các toán tử so sánh luôn trả về giá trị Boolean
a = 5
b = 10

# So sánh lớn hơn
greater_than_result = a > b  # Kết quả: False

# So sánh lớn hơn hoặc bằng
greater_than_equal_result = a >= b  # Kết quả: False

# So sánh nhỏ hơn
less_than_result = a < b  # Kết quả: True

# So sánh nhỏ hơn hoặc bằng
less_than_equal_result = a <= b  # Kết quả: True
