# Các phép toán số học
a = 10
b = 3

# Cộng
sum_result = a + b  # Kết quả: 13

# Trừ
difference_result = a - b  # Kết quả: 7

# Nhân
product_result = a * b  # Kết quả: 30

# Chia thường
division_result = a / b  # Kết quả: 3.3333333333333335

# Chia lấy dư
modulus_result = a % b  # Kết quả: 1

# Lũy thừa
exponential_result = a ** b  # Kết quả: 1000
